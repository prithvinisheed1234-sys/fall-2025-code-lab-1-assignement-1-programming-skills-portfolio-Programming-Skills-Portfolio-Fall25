# asking the user the question and storing the input in the variable answer.
answer = input("What is the capital of France?")
# checking if the answer is correct or not and printing the appropriate message.
if answer == "Paris":
    # printing the message if the answer is correct.
    print("The answer is correct")
# printing the message if the answer is incorrect.
else:
    # printing the message if the answer is incorrect.
    print("The answer is incorrect")